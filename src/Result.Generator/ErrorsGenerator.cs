using System.Collections.Immutable;
using System.Linq;
using System.Threading;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using PxBunny.Result.Generator.Extensions;

namespace PxBunny.Result.Generator;

using InitializationContext = IncrementalGeneratorInitializationContext;
using Input = (Compilation Compilation, ImmutableArray<ClassDeclarationSyntax> List);

[Generator]
internal sealed class ErrorsGenerator : IIncrementalGenerator
{
    public void Initialize(InitializationContext context)
    {
        var syntaxProvider = context.SyntaxProvider.CreateSyntaxProvider(Predicate, Transform);
        var compilationProvider = context.CompilationProvider;
        var input = compilationProvider.Combine(syntaxProvider.Collect());
        context.RegisterSourceOutput(input, Execute);
    }

    private static void Execute(SourceProductionContext context, Input input)
    {
        if (!input.List.Any()) return;

        const int bodyIndent = 8;
        var assembly = typeof(ErrorsGenerator).Assembly;
        var version = assembly.GetName().Version;
        var errorsNamespace = assembly.GetMetadata("ErrorsNamespace");

        var source = $$"""
            // <auto-generated/>
            using System.CodeDom.Compiler;

            namespace {{errorsNamespace}}
            {
                [GeneratedCode("{{typeof(ErrorsGenerator).FullName}}", "{{version}}")]
                public static partial class Errors
                {
            {{GenerateClassBody(input).AddIndent(bodyIndent)}}
                }
            }
            """;

        context.AddSource("Errors.g.cs", source);
    }

    private static bool Predicate(SyntaxNode node, CancellationToken _) =>
        node is ClassDeclarationSyntax classDeclaration && classDeclaration.IsErrorType();

    private static ClassDeclarationSyntax Transform(GeneratorSyntaxContext context, CancellationToken _) =>
        (ClassDeclarationSyntax)context.Node;

    private static string GenerateClassBody(Input input)
    {
        var (compilation, list) = input;

        return string.Join("\n\n", list.Select(node =>
        {
            var isFileScoped = node.Modifiers.Any(m => m.IsKind(SyntaxKind.FileKeyword));

            if (isFileScoped) return null;

            var fullName = node.GetFullName(compilation.GetSemanticModel(node.SyntaxTree));
            var className = node.Identifier.Text;
            var parameters = node.GetConstructorParameters();
            var accessModifier = node.Modifiers.Any(m => m.IsKind(SyntaxKind.InternalKeyword)) ? "internal" : "public";
            var methods = parameters.Select(p => GenerateMethod(fullName, className, accessModifier, p));
            return string.Join("\n\n", methods);
        }).Where(str => !string.IsNullOrWhiteSpace(str)));
    }

    private static string GenerateMethod(
        string fullTypeName,
        string methodName,
        string accessModifier,
        SeparatedSyntaxList<ParameterSyntax> parameters)
    {
        var argsWithTypes = string.Join(", ", parameters.Select(p => p.ToString()));
        var args = string.Join(", ", parameters.Select(p => p.Identifier.Text));
        return $"{accessModifier} static {fullTypeName} {methodName}({argsWithTypes}) {{ return new {fullTypeName}({args}); }}";
    }
}
